/*
Assignment operators
x = y -> x = y
x += y -> x = x + y
x -= y -> x = x - y
x *= y -> x = x * y
x /= y -> x = x / y
x %= y -> x = x % y (remainder of x/y)
x **= y -> x = x ** y
x <<= y -> x = x << y (left shift)
x >>= y -> x = x >> y (right shift)
*/
document.write(`<h1>Assignment operators</h1><br>`);
document.write(`let num = 10;<br>`);
document.write(`num += 10;<br>`);
let num = 10;
num += 10;
document.write("num = " + num + "<br>");

/*
Arithmetic operators
+ -> Addition
- -> Subtraction
* -> Multiplication
/ -> Division
% -> Modulus (remainder of x/y)
** -> Exponentiation
++ -> Increment
-- -> Decrement
*/
document.write(`<h1>Arithmetic operators</h1><br>`);
document.write(`let num = 20;<br>`);
document.write(`num++;<br>`);
num = 20;
num++;
document.write("num = " + num + "<br>");